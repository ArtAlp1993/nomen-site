// Текстовая инструкция для Claude: параметры + диапазоны + пресеты + текущее
// состояние. Модель возвращает ТОЛЬКО патч (JSON) + короткий ответ.
export function formulaPromptRu(cfg) {
  const lines = [];
  for (const [section, items] of Object.entries(FORMULA)) {
    lines.push(`### ${section}`);
    for (const [key, label, min, max, , effect] of items) {
      lines.push(`- ${key} (${label}): ${min}…${max} — ${effect}`);
    }
  }
  return [
    "Ты — помощник в «лаборатории шарика» сайта NOMEN. Шарик — 3D-радужка из светящихся волокон.",
    "Пользователь просит изменить его вид (голосом, текстом или референс-картинкой).",
    "Верни JSON: { patch: {только изменяемые параметры}, reply: 'короткий ответ по-русски' }.",
    "",
    "## Параметры (ключ, диапазон, эффект)",
    ...lines,
    "- colors: массив из РОВНО 5 hex-цветов волокон; последний — редкий акцент-«искры».",
    "",
    "## Готовые пресеты (если просят похожее — можно взять за основу и настроить)",
    ...Object.entries(PRESETS).map(
      ([name, p]) =>
        `- «${name}»: ${JSON.stringify(
          Object.fromEntries(
            Object.entries(p).filter(
              ([k, v]) => JSON.stringify(DEFAULT_CONFIG[k]) !== JSON.stringify(v)
            )
          )
        )}`
    ),
    "",
    "## Текущий конфиг",
    JSON.stringify(cfg),
    "",
    "Если пришла картинка-референс: извлеки её палитру (5 hex) и характер (плотность, разлёт, движение) и собери patch под неё.",
    "Меняй только то, о чём просят. reply — 1 короткое предложение.",
  ].join("\n");
}
