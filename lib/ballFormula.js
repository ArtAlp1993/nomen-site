// Обрезать значение патча в допустимый диапазон параметра.
export function clampPatch(patch) {
  const limits = {};
  for (const items of Object.values(FORMULA)) {
    for (const [key, , min, max] of items) limits[key] = { min, max };
  }
  const out = {};
  for (const [k, v] of Object.entries(patch || {})) {
    if (k === "colors") {
      if (
        Array.isArray(v) &&
        v.length === 5 &&
        v.every((c) => /^#[0-9a-fA-F]{6}$/.test(c))
      ) {
        out.colors = v;
      }
    } else if (limits[k] && typeof v === "number" && isFinite(v)) {
      out[k] = Math.min(limits[k].max, Math.max(limits[k].min, v));
    }
  }
  return out;
}

